    public void autonum(){
       try{
           String sql = "SELECT MAX(RIGHT(id_buku,2)) AS no_auto FROM buku ";
           st = net.createStatement();
           rs = st.executeQuery(sql);
           
           if(rs.next()){
               String no_auto,nol_plus;
               int p;
               no_auto = Integer.toString(rs.getInt(1)+1);
               p = no_auto.length();
               nol_plus = "";
               for(int i =0;i<=2 -p;i++){
                   nol_plus = nol_plus+"0";
               }
               ibuku.setText("BK"+nol_plus+no_auto);
           }
       } catch (Exception e) {
           ibuku.setText("BK001");
       }
       }
   


