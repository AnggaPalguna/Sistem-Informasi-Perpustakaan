    private void tampilData(String string) {
         tabModel = (DefaultTableModel)tbbuku.getModel();
         tabModel.setRowCount(0);
         try {
             st = net.createStatement();
             rs = st.executeQuery("SELECT * FROM buku ");
             String id_buku;
             String judul;
             String ket_buku;
             String stat_buku;
             String id_kategori;
             while(rs.next()){
                 id_buku = rs.getString("id_buku");
                 judul = rs.getString("judul");
                 ket_buku = rs.getString("ket_buku");
                 stat_buku = rs.getString("stat_buku");
                 id_kategori = rs.getString("id_kategori");
                 tabModel.addRow(new Object[]{id_buku, judul, ket_buku,stat_buku, id_kategori });
             }
             
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
         }
         }
         
