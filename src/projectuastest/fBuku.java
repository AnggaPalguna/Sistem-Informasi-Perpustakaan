    private void tbbukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbbukuMouseClicked
        // TODO add your handling code here:
        ibuku.setText(tbbuku.getValueAt(tbbuku.getSelectedRow(),0).toString());
        bnama.setText(tbbuku.getValueAt(tbbuku.getSelectedRow(),1).toString());
        desbuku.setText(tbbuku.getValueAt(tbbuku.getSelectedRow(),2).toString());
        statbuku.setText(tbbuku.getValueAt(tbbuku.getSelectedRow(),3).toString());
        ikategori.setSelectedItem(tbbuku.getValueAt(tbbuku.getSelectedRow(),4).toString());
        tambah.setEnabled(true);
        ubah.setEnabled(true);
        hapus.setEnabled(true);
    }//GEN-LAST:event_tbbukuMouseClicked

