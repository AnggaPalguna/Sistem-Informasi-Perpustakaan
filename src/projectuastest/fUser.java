    private void tampilData(String string) {
         tabModel = (DefaultTableModel)tbuser.getModel();
         tabModel.setRowCount(0);
         try {
             st = net.createStatement();
             rs = st.executeQuery("SELECT * FROM user ");
             String id_user;
             String nama_user;
             String telepon_user;
             String alamat_user;
             String ket_user;
             while(rs.next()){
                 id_user = rs.getString("id_user");
                 nama_user = rs.getString("nama_user");
                 telepon_user = rs.getString("telepon_user");
                 alamat_user = rs.getString("alamat_user");
                 ket_user = rs.getString("ket_user");
                 tabModel.addRow(new Object[]{id_user, nama_user,telepon_user,alamat_user,ket_user });
             }
             
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e.getMessage());
         }
         }
    
